    <div class="article">
      <h2><i class="title-box"></i>神奇峡湾</h2>
      <p class="desc">米尔德福佛峡湾，是新西兰峡湾国家公园14条峡湾最受欢迎的旅游目的地，也是联合国科教文组织列入世界遗产UNESCO的自然保护区。从蒂阿瑙进入峡湾国家公园的唯一道路,沿途景观绝美,全长120公里的－米佛景观大道－Mildford Road－是一条唯一能让车辆通往进入峡湾国家公园到达米佛峡湾的道路。搭乘–米佛峡湾游船游览艇巡游–深谷峡湾中，瀑布潺流、白云悠悠、如诗如画的景色，令人激赏，航程中您有机会可观赏特有的寒带动物：如海豹、海豚、企鹅等。</p>

      <img src="http://hitour.qiniudn.com/8c77eac5d0979be2ee9f985f5db206da.jpg" alt=""/>
      <h3>惊险壮美的神奇峡湾</h3>
      <p class="img-desc">从皇后镇开往米尔福德的公路是新西兰最美的一条公路，这条公路不仅仅是一条通往米尔福德峡湾（Milford Sound）的通道，同时一段深入峡湾国家公园（Fiordland National Park）和蒂瓦希普纳姆世界遗产保护区（Te Wahipounamu World Heritage area）的难忘之旅。米尔福德峡湾(Milford Sound)国家公园，规模最大且保存最完美自然景观的峡湾。峡湾国家公园这块未经人类文明破坏的土地作为自然遗产，每当下雨的时候，就会出现许多“暂时的瀑布”，这些瀑布从峭壁表面倾泻而下，有的长达1000米。</p>
